fun main()
{
    // Arithmetic Operators = { + , _ , / , * }
    var a = 10
    val b = 20
    val c = a + b
    println("The result is $c")

    a += 10
    val d = 30
    println("The result is ${a + d}")

    // Comparison Operator = { == , != , < , > , >= , <= }
    println( a == b )
    println ( a > b )

    // Logical Operator = { && , || , !}
    val x = 10
    val y = 20
    println(x < y && !(x > y))




}
